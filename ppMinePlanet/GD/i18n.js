function T(key) {
  return (STRINGS[currentLang]||STRINGS.en)[key] || STRINGS.en[key] || key;
}
function setLang(lang) {
  currentLang = lang;
  document.querySelectorAll('#lang-select,#lang-select-start').forEach(s => s.value = lang);
  try { localStorage.setItem(LANG_KEY, lang); } catch(e) {}
  applyI18n();
}
function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if (el.tagName==='INPUT') { el.placeholder = T(k).replace(/<[^>]+>/g,''); return; }
    el.innerHTML = T(k);
  });
  const br=document.getElementById('b-reveal'); if(br) br.innerHTML=T('btn_reveal');
  const bc=document.getElementById('b-collect'); if(bc) bc.innerHTML=T('btn_collect');
  const bf=document.getElementById('b-flag'); if(bf) bf.innerHTML=T('btn_flag');
  const bq=document.getElementById('b-quit'); if(bq&&!gameAbandoned) bq.innerHTML=T('btn_quit');
  const bm=document.getElementById('b-menu'); if(bm) bm.innerHTML=T('btn_menu');
  const sp=document.getElementById('start-play-btn'); if(sp) sp.innerHTML=T('start_play');
  const sh=document.getElementById('start-hist-btn'); if(sh) sh.innerHTML=T('start_hist');
  const ss=document.getElementById('start-shop-btn'); if(ss) ss.innerHTML=T('start_shop');
  const shelp=document.getElementById('start-help-btn'); if(shelp) shelp.innerHTML=T('start_help');
  const sn=document.getElementById('start-name'); if(sn) sn.placeholder=T('start_name_ph').replace(/<[^>]+>/g,'');
  const ab=document.getElementById('arcade-toggle-btn'); if(ab) ab.textContent=arcadeMode?T('arcade_on'):T('arcade_off');
  const pl=document.getElementById('player-label'); if(pl) pl.textContent=T('hud_player');
  const cl=document.querySelector('#coins-hud .top-label'); if(cl) cl.textContent=T('hud_coins');
  const st2=document.getElementById('shop-title'); if(st2) st2.textContent=T('shop_title');
  const wh=document.querySelector('#sc-level h1'); if(wh) wh.textContent=T('win_title').replace(/<[^>]+>/g,'');
  const gh=document.querySelector('#sc-over h1'); if(gh) gh.textContent=T('go_title').replace(/<[^>]+>/g,'');
  const gp=document.querySelector('#sc-over p'); if(gp) gp.textContent=T('go_msg').replace(/<[^>]+>/g,'');
}


function t(key, vars = {}) {
  const str = (LANGS[currentLang] && LANGS[currentLang][key]) || (LANGS.en && LANGS.en[key]) || key;
  return str.replace(/\{(\w+)\}/g, (_, k) => vars[k] !== undefined ? vars[k] : `{${k}}`);
}

function setLang(lang) {
  if (!LANGS[lang]) return;
  currentLang = lang;
  localStorage.setItem(LANG_KEY, lang);
  applyI18n();
}

function getLangList() {
  return Object.entries(LANGS).map(([code, data]) => ({ code, label: data.label }));
}
